export interface UterusData {
  time: number;
  uterus: number;
}

export function generateDemoData(durationSec = 600): { bpm: BpmData[]; uterus: UterusData[] } {
  const bpm: BpmData[] = [];
  const uterus: UterusData[] = [];

  let baseline = 140;

  for (let t = 0; t < durationSec * 10; t++) {
    //    baseline
    let val = baseline + 5 * Math.sin(t / 20) + (Math.random() * 2 - 1);

    //   120180
    if (t >= 200 && t < 800) val -= 40;

    //   300360
    if (t >= 3000 && t < 3600) val += 30;

    //     200220
    if (t >= 2000 && t < 2200) val -= 25;

    bpm.push({ time: t / 10, bpm: Math.max(60, Math.min(200, val)) });

    // uterus:  510,   ~150
    let u = 5 + Math.random() * 2;
    if ((t % 1500) > 200 && (t % 1500) < 600) {
      u += 30 + 10 * Math.sin((t % 40) / 40 * Math.PI); // 
    }
    uterus.push({ time: t / 10, uterus: u });
  }

  return { bpm, uterus };
}


